package com.park.einvoice.domain.response;

public class EnterpriseRegPTResp {
	private String code, msg, sign, serialNo;
	private EnterpriseRegPTRespContent content;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	public String getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
	public EnterpriseRegPTRespContent getContent() {
		return content;
	}
	public void setContent(EnterpriseRegPTRespContent content) {
		this.content = content;
	}
	@Override
	public String toString() {
		return "EnterpriceRequest [code=" + code + ", msg=" + msg + ", sign=" + sign + ", serialNo=" + serialNo
				+ ", content=" + content + "]";
	}
	
}
