package com.park.einvoice.domain.response;

public class EnterpriseRegPTRespContent {
	private String taxpayerNum, enterpriceName;

	public String getTaxpayerNum() {
		return taxpayerNum;
	}

	public void setTaxpayerNum(String taxpayerNum) {
		this.taxpayerNum = taxpayerNum;
	}

	public String getEnterpriceName() {
		return enterpriceName;
	}

	public void setEnterpriceName(String enterpriceName) {
		this.enterpriceName = enterpriceName;
	}

	@Override
	public String toString() {
		return "EnterpriceContent [taxpayerNum=" + taxpayerNum + ", enterpriceName=" + enterpriceName + "]";
	}
	
}
